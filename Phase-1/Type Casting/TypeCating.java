package Project;

public class TypeCasting {
	public static void main(String[] args) {
		
		short k=20;
		int l=k;
		System.out.println( "implicit typecasting"+l);

		int a=10;
		double b=a;
System.out.println( "implicit typecasting"+b);

double a1=10.5;
int b1=(int)a1;
System.out.println("Explicit typecasting"+b1);

float u=80;
int j=(int)u;
System.out.println("Explicit typecasting"+j);

	}
	

}
